# Example: Checking the grade based on a student's score

# Input: Student's score
score = 85

# Checking the grade
if score >= 50:
    grade = 'F'
elif score >= 60:
    grade = 'D'
elif score >= 70:
    grade = 'C'
elif score >= 80:
    grade = 'B'
else:
    grade = 'A'

# Output: Displaying the grade
print("The student's grade is", grade)
